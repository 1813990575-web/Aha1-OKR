import React from 'react';

interface SidebarRailProps {
  activeItem?: string;
  onItemClick?: (item: string) => void;
}

export function SidebarRail({ activeItem = 'focus', onItemClick }: SidebarRailProps) {
  const menuItems = [
    { id: 'focus', icon: ClockIcon, label: '专注' },
    { id: 'archive', icon: ArchiveIcon, label: '归档' },
    { id: 'stats', icon: ChartIcon, label: '统计' },
    { id: 'book', icon: BookIcon, label: '手册' },
    { id: 'star', icon: StarIcon, label: '收藏' },
  ];

  const bottomItems = [
    { id: 'settings', icon: SettingsIcon, label: '设置' },
  ];

  return (
    <div className="w-16 h-full bg-stone-100/80 backdrop-blur-sm border-r border-stone-200/60 flex flex-col items-center py-4 flex-shrink-0">
      {/* Top Spacer for macOS traffic lights */}
      <div className="h-8 w-full" style={{ WebkitAppRegion: 'drag' } as React.CSSProperties} />

      {/* Main Menu Items */}
      <nav className="flex-1 flex flex-col items-center gap-4 w-full px-2">
        {menuItems.map((item) => {
          const Icon = item.icon;
          const isActive = activeItem === item.id;
          return (
            <button
              key={item.id}
              onClick={() => onItemClick?.(item.id)}
              className={`w-10 h-10 rounded-xl flex items-center justify-center transition-all duration-200 group relative ${
                isActive
                  ? 'text-stone-800'
                  : 'text-stone-400 hover:bg-stone-200/60 hover:text-stone-600'
              }`}
              title={item.label}
            >
              <Icon className="w-5 h-5" />
              {/* Active indicator dot - 红色圆点 */}
              {isActive && (
                <span className="absolute -bottom-1 w-1.5 h-1.5 bg-red-500 rounded-full" />
              )}
            </button>
          );
        })}
      </nav>

      {/* Bottom Items */}
      <div className="flex flex-col items-center gap-1 w-full px-2 pb-2">
        {bottomItems.map((item) => {
          const Icon = item.icon;
          const isActive = activeItem === item.id;
          return (
            <button
              key={item.id}
              onClick={() => onItemClick?.(item.id)}
              className={`w-10 h-10 rounded-xl flex items-center justify-center transition-all duration-200 relative ${
                isActive
                  ? 'text-stone-800'
                  : 'text-stone-400 hover:bg-stone-200/60 hover:text-stone-600'
              }`}
              title={item.label}
            >
              <Icon className="w-5 h-5" />
              {/* Active indicator dot - 红色圆点 */}
              {isActive && (
                <span className="absolute -bottom-1 w-1.5 h-1.5 bg-red-500 rounded-full" />
              )}
            </button>
          );
        })}
      </div>
    </div>
  );
}

